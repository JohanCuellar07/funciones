//con Parámetros
function porce(pnota, pnumPor){
    let porcen;
    let nota = pnota;
    let numPor = pnumPor;
    porcen = nota * numPor;
    return porcen;
}

//como Expresión
const porceExp = function(pnota, pnumPor){
    let porcen;
    let nota = pnota;
    let numPor = pnumPor;
    porcen = nota * numPor;
    return porcen;
}