//con Parámetros
function suma(pnumUno, pnumDos){
    let numUno = pnumUno;
    let numDos = pnumDos;
    let sumar;
    sumar = numUno + numDos;
    return sumar;
}
function resta(pnumUno, pnumDos){
    let numUno = pnumUno;
    let numDos = pnumDos;
    let restar;
    restar = numUno - numDos;
    return restar;
}
function multiplicacion(pnumUno, pnumDos){
    let numUno = pnumUno;
    let numDos = pnumDos;
    let multiplicar;
    multiplicar = numUno * numDos;
    return multiplicar;
}
function division(pnumUno, pnumDos){
    let numUno = pnumUno;
    let numDos = pnumDos;
    let dividir;
    dividir = numUno / numDos;
    return dividir;
}

